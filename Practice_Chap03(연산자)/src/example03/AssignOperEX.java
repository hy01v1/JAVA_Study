package example03;

public class AssignOperEX {

	public static void main(String[] args) {

		//복합대입연산자 연습
		// += =+ /=
		
		int result = 0;
		result += 10;
		// result = result + 10
		System.out.println("result의 결과 : " + result);
		result -= 5;
		System.out.println("result의 결과 : " + result);
		result *= 5;
		System.out.println("result의 결과 : " + result);
		result /= 5;
		System.out.println("result의 결과 : " + result);
		result %= 5;
		System.out.println("result의 결과 : " + result);
		
		
		
		
		
		
	}

}
