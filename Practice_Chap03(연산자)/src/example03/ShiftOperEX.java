package example03;

public class ShiftOperEX {

	public static void main(String[] args) {
		
		// left Shift => x * 2의 n승
		// right Shift => x / 2의 n승
		
		int a = 8;
		int b = 2;
		
		System.out.println("8 << 2 : " + (a<<b));
		System.out.println("8 >> 2 : " + (a>>b));
		// 연산 속도가 신속하여 그래픽 분야에 많이 사용된다.
		
		
		
	}

}
